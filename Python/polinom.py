# 2. Dereceden Polinom Çözümü 
print("Açıklama:\n-----------------\nKullanıcıdan a, b ve c sayıları alınacaktır ve 2. Dereceden Polinom hesaplanıcaktır\n--------------------------------- ")
a = int(input("Lütfen a sayısını giriniz : "))
b = int(input("Lütfen b sayısını giriniz : "))
c = int(input("Lütfen c sayısını giriniz : "))
print("Sonuç = {}".format( (a**2) + (b**2) + 3*c ))

#Kullanıcıdan a,b ve c sayılarını okuyarak aşağıdaki işlemi yapan kodu yazınız a'nın karesi +b'nin karesi +3c